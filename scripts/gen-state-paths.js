const topojson = require('topojson-client');
const us = require('us-atlas/states-albers-10m.json');
const fs = require('fs');

const fipsToAbbr = {
  '01':'AL','02':'AK','04':'AZ','05':'AR','06':'CA','08':'CO','09':'CT',
  '10':'DE','11':'DC','12':'FL','13':'GA','15':'HI','16':'ID','17':'IL',
  '18':'IN','19':'IA','20':'KS','21':'KY','22':'LA','23':'ME','24':'MD',
  '25':'MA','26':'MI','27':'MN','28':'MS','29':'MO','30':'MT','31':'NE',
  '32':'NV','33':'NH','34':'NJ','35':'NM','36':'NY','37':'NC','38':'ND',
  '39':'OH','40':'OK','41':'OR','42':'PA','44':'RI','45':'SC','46':'SD',
  '47':'TN','48':'TX','49':'UT','50':'VT','51':'VA','53':'WA','54':'WV',
  '55':'WI','56':'WY'
};

function ringToPath(ring) {
  let d = 'M';
  for (let i = 0; i < ring.length; i++) {
    const [x, y] = ring[i];
    if (i === 0) d += x.toFixed(1) + ',' + y.toFixed(1);
    else d += 'L' + x.toFixed(1) + ',' + y.toFixed(1);
  }
  return d + 'Z';
}

function geomToPath(geom) {
  if (!geom) return '';
  if (geom.type === 'Polygon') {
    return geom.coordinates.map(ringToPath).join('');
  }
  if (geom.type === 'MultiPolygon') {
    return geom.coordinates.map(poly => poly.map(ringToPath).join('')).join('');
  }
  return '';
}

const geo = topojson.feature(us, us.objects.states);
const results = {};
for (const f of geo.features) {
  const abbr = fipsToAbbr[f.id];
  if (abbr) results[abbr] = geomToPath(f.geometry);
}

const sorted = Object.entries(results).sort(([a], [b]) => a.localeCompare(b));

let out = '// Auto-generated by scripts/gen-state-paths.js — do not edit manually\n';
out += '// Source: us-atlas/states-albers-10m.json (Albers USA projection, 975x610 viewBox)\n';
out += 'export const STATE_PATHS: Record<string, string> = {\n';
for (const [abbr, d] of sorted) {
  // Use JSON.stringify so the string is safely quoted with no template literal issues
  out += '  ' + abbr + ': ' + JSON.stringify(d) + ',\n';
}
out += '};\n';

fs.writeFileSync('lib/statePaths.ts', out);
console.log('Written lib/statePaths.ts with', sorted.length, 'states');
